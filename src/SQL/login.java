    void logearse() throws SQLServerException {

        SQLServerDataSource ds = new SQLServerDataSource();
        ds.setServerName("LAPTOP-NVLTKD07");
        ds.setDatabaseName("PRESTAMOS");
        ds.setUser("sa");
        ds.setPassword("sa");
        ds.setPortNumber(1433);

        try {
            Connection con = ds.getConnection();
            PreparedStatement ps = con.prepareStatement("SELECT * FROM ADMINISTRADOR WHERE nom_admin = ? AND contraseña = ?");

            ps.setObject(1, this.txtUsuario.getText());
            ps.setObject(2, this.txtPassword.getText());
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                new inicio_admin().setVisible(true);

            } else{
                System.out.println("ERROR AL INICIAR SESION");
            
            }

        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


